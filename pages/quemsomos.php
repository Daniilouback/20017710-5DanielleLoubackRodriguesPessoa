<p>Nossa História

A empresa DOCE AÇAÍ foi criada em 2021, em NOVA LIMA, MG. É especializada em açaí, com diferentes opções de combinações e adicionais com sugestões que agradam ao público consumidor.

Nossos Princípios

Ética, respeito e valorização dos colaboradores, fornecedores e clientes. Considerar que o relacionamento harmonioso com os clientes e parceiros é essencial para o sucesso, a continuidade e o crescimento da empresa. Buscar constante aperfeiçoamento tecnológico e melhoria dos processos.

Nossa Missão

Proporcionar saúde, bem estar e energia, oferecendo os melhores produtos à base de Açaí em ambiente agradável que permita bem estar, lazer e interação entre as pessoas e famíliares.

Nossa Visão

Ser reconhecida nacionalmente como a melhor empresa de processamento, preparação e montagem de açaí, com o mesmo padrão de qualidade e serviço.</p>

